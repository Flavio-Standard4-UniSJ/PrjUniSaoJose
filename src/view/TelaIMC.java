    private void btnCalcularIMCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularIMCActionPerformed
        MeuIMC m = new MeuIMC(txtNome.getText(), txtDataNascimento.getText());
        float peso = m.setPeso(Float.parseFloat(txtPeso.getText()));
        float altura = m.setAltura(Float.parseFloat(txtAltura.getText()));
        

        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
        Date nascimento = null;
        try {
            nascimento = sdf.parse(m.getNascimento());
        } catch (ParseException e) {
            System.out.println(e.getMessage());
        }

        Calendar calNascimento = Calendar.getInstance();
        calNascimento.setTime(nascimento);

        int anoNascimento = calNascimento.get(Calendar.YEAR);

        Calendar calAtual = Calendar.getInstance();
        calAtual.setTime(new Date()); // Pega a data atual
        int anoAtual = calAtual.get(Calendar.YEAR);

        int idade = anoAtual - anoNascimento;

        float imc = m.calcularIMC(peso, altura);
        
        m.setResultado(m.classificarIMC(imc));
        
        JOptionPane.showMessageDialog(null, "Nome: " + m.getNome() + "\n Idade:  " + idade + "\n IMC: " + imc  + "\n Resultado: " + m.getResultado());


        try {
            MeuImcDAO meuImcDAO = new MeuImcDAO();
            meuImcDAO.cadastrarIMC(m);
            JOptionPane.showMessageDialog(null, "Novo usuário adicionado");
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
        }

    }//GEN-LAST:event_btnCalcularIMCActionPerformed

